// Regenerates the Overpass-600 advance-width table the card kit measures with,
// from the real bundled font — the same tooling the logo outliner uses. This is
// how the card kit keeps crisp VECTOR text AND exact, deterministic layout: text
// renders as real `<text font-family="Overpass" font-weight="600">`, and width is
// summed from these per-glyph advances (font units, upm 2000). Run from web/:
//   node scripts/gen-card-metrics.mjs
// Writes: ../src/art/overpassMetrics.ts  (generated; do not hand-edit)

import { readFileSync, writeFileSync } from 'node:fs';
import { create } from 'fontkitten';

const FONT = 'public/fonts/overpass-600.woff2';
const font = create(readFileSync(FONT));
if (font.unitsPerEm !== 2000) throw new Error(`unexpected upm ${font.unitsPerEm}`);

// ASCII printable + the punctuation the house voice uses (em/en dash, curly
// quotes, middot, bullet, ellipsis, common currency).
const extra = ['–', '—', '‘', '’', '“', '”', '·', '•', '…', '€', '£', '¥', '™', '©', '®', '°', '×'];
const chars = [];
for (let cp = 0x20; cp <= 0x7e; cp++) chars.push(String.fromCodePoint(cp));
chars.push(...extra);

const advance = {};
for (const ch of chars) {
  const cp = ch.codePointAt(0);
  if (!font.hasGlyphForCodePoint(cp)) continue;
  advance[ch] = Math.round(font.glyphForCodePoint(cp).advanceWidth);
}
// A sane fallback for any glyph the caller measures that we didn't table.
const defaultAdvance = advance['o'] ?? Math.round(font.unitsPerEm * 0.55);

const entries = Object.entries(advance)
  .map(([ch, w]) => `    ${JSON.stringify(ch)}: ${w},`)
  .join('\n');

const out = `// GENERATED by web/scripts/gen-card-metrics.mjs — do not edit by hand.
// Advance widths (font units, unitsPerEm ${font.unitsPerEm}) for Overpass 600, so
// the card kit measures crisp vector text exactly. Regenerate if the font changes.

import type { FontMetric } from './fontMetrics';

export const OVERPASS_600: FontMetric = {
  family: 'Overpass',
  weight: 600,
  unitsPerEm: ${font.unitsPerEm},
  capHeight: ${Math.round(font.capHeight)},
  ascent: ${Math.round(font.ascent)},
  descent: ${Math.round(font.descent)},
  defaultAdvance: ${defaultAdvance},
  advance: {
${entries}
  },
};
`;

writeFileSync('../src/art/overpassMetrics.ts', out);
console.log(`wrote ../src/art/overpassMetrics.ts — ${Object.keys(advance).length} glyphs`);
